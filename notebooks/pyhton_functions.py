# create a function to addition of two numbers
def addition(num1: float, num2: float) -> float:
    """
    Adds two numbers together.

    Parameters
    ----------
    num1 : float
        The first number.
    num2 : float
        The second number.

    Returns
    -------
    float
        The sum of num1 and num2.

    Examples
    --------
    >>> addition(1.0, 2.0)
    3.0
    """
    return num1 + num2
